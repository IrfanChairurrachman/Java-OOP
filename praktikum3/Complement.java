public class Complement {
    public static void main(String[] args) {
        int i = 7;
        System.out.println("Nilai: " +i);
        System.out.println("Biner: " + Integer.toBinaryString(i));
        System.out.println("Hasil operasi ~ : " + ~i);
        System.out.println("Biner: " + Integer.toBinaryString(~i));
    }
}
